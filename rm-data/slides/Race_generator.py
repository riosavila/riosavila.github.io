def get_all_races():
    """
    Returns a flattened list of all races and sub-races.
    
    Returns:
        list: Comprehensive list of all races and sub-races
    """
    all_races = []
    for main_race, sub_races in FANTASY_RACE_SUBTYPES.items():
        all_races.append(main_race)
        for sub_race_category, sub_race_list in sub_races.items():
            all_races.append(sub_race_category)
            all_races.extend(sub_race_list)
    return all_races

def main():
    print("Comprehensive Fantasy Race Subtypes:")
    for main_race, sub_races in FANTASY_RACE_SUBTYPES.items():
        print(f"\n{main_race.upper()} Sub-Races:")
        for sub_race_category, sub_race_list in sub_races.items():
            print(f"\n  {sub_race_category.replace('_', ' ').title()}:")
            for sub_race in sub_race_list:
                print(f"    - {sub_race}")
    
    print("\n\nTotal Number of Races:", len(get_all_races()))

if __name__ == "__main__":
    main()